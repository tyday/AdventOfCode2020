import pytest
import Day20

def test_ImageTile():
    input = ['Tile 1951:',
        '#.##...##.',
        '#.####...#',
        '.....#..##',
        '#...######',
        '.##.#....#',
        '.###.#####',
        '###.##.##.',
        '.###....#.',
        '..#.#..#.#',
        '#...##.#..'
        ]
    tile = Day20.ImageTile(input)
    assert tile.name == '1951'
    assert tile.tile[0] == ['#', '.', '#', '#', '.', '.', '.', '#', '#', '.']
    assert tile.flipped_tile[0] == ['.', '#', '#', '.', '.', '.', '#', '#', '.', '#']
    assert tile.sides == [['#', '.', '#', '#', '.', '.', '.', '#', '#', '.'],
                        ['.', '#', '#', '#', '#', '#', '.', '.', '#', '.'],
                        ['#', '.', '.', '.', '#', '#', '.', '#', '.', '.'],
                        ['#', '#', '.', '#', '.', '.', '#', '.', '.', '#']]
